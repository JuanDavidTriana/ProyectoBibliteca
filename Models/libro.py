class Libro:
    def __init__(self,id, id_seccion, nombre, genero, prologo, isbn, autores, id_biblioteca):
        self.id = id
        self.id_seccion = id_seccion
        self.nombre = nombre
        self.genero = genero
        self.prologo = prologo
        self.isbn = isbn
        self.autores = autores
        self.id_biblioteca = id_biblioteca